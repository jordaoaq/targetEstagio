# 5) Você está em uma sala com três interruptores, cada um conectado a uma lâmpada em salas
# diferentes. Você não pode ver as lâmpadas da sala em que está, mas pode ligar e desligar os
# interruptores quantas vezes quiser. Seu objetivo é descobrir qual interruptor controla qual
# lâmpada. Como você faria para descobrir, usando apenas duas idas até uma das salas das lâmpadas,
# qual interruptor controla cada lâmpada?  
#
# Caso as lâmpadas sejam incandescente apenas uma visita é necessária. Ligaria o interruptor 1, esperaria
# 5 minutos, desligaria o interruptor 1 e ligaria o interruptor 2. Ao visitar a sala das lâmpadas, a que estiver
# acesa é a lâmpada ligada ao interruptor 2, a que estiver apagada e quente está ligada ao interruptor 1
# e a que estiver apagada e fria é ligada ao interruptor 3.
# Caso as lâmpadas não sejam de um material que esquenta, basta ligar o interruptor 1, visitar a sala. Isso
# dirá qual a lâmpada ligada ao interruptor 1. Voltar, desligar o interruptor 1, ligar o interruptor 3 e voltar
# à sala das lâmpandas. A làmpada que estiver acesa é ligada ao interruptor 3 e, por eliminação, a que nunca foi acesa
# é ligada ao interruptor 2.